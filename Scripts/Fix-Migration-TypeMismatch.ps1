# MASTER SCRIPT: Backup Database & Drop Views
# Safe way to fix migration type mismatch error

$ErrorActionPreference = "Stop"

Write-Host ""
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?  ?? FIX MIGRATION TYPE MISMATCH - SAFE METHOD         ?" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

Write-Host "This script will:" -ForegroundColor White
Write-Host "  1. ? Backup your database (safe!)" -ForegroundColor Green
Write-Host "  2. ? Drop old buggy views" -ForegroundColor Green
Write-Host "  3. ? Allow fresh migrations to run" -ForegroundColor Green
Write-Host ""

$response = Read-Host "Continue? (Y/N)"
if ($response -ne 'Y' -and $response -ne 'y') {
    Write-Host "Cancelled." -ForegroundColor Yellow
    exit 0
}

Write-Host ""
Write-Host "???????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "STEP 1: BACKUP DATABASE" -ForegroundColor Cyan
Write-Host "???????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

# Run backup script
$backupScript = Join-Path $PSScriptRoot "Quick-Backup-Before-Migration.ps1"
if (Test-Path $backupScript) {
    & $backupScript
    if ($LASTEXITCODE -ne 0) {
        Write-Host "? Backup failed! Not proceeding with view drops." -ForegroundColor Red
        exit 1
    }
}
else {
    Write-Host "? Backup script not found: $backupScript" -ForegroundColor Red
    exit 1
}

Write-Host ""
Write-Host "???????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "STEP 2: DROP OLD VIEWS" -ForegroundColor Cyan
Write-Host "???????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

Start-Sleep -Seconds 2

# Run drop views script
$dropScript = Join-Path $PSScriptRoot "Drop-Old-Views.ps1"
if (Test-Path $dropScript) {
    & $dropScript
    if ($LASTEXITCODE -ne 0) {
        Write-Host "? Drop views failed!" -ForegroundColor Red
        Write-Host "??  Your backup is safe at C:\Surveillance-Backups\BeforeMigrationFix" -ForegroundColor Yellow
        exit 1
    }
}
else {
    Write-Host "? Drop script not found: $dropScript" -ForegroundColor Red
    exit 1
}

Write-Host ""
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor Green
Write-Host "?  ? ALL DONE - READY TO RUN!                          ?" -ForegroundColor Green
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor Green
Write-Host ""
Write-Host "?? Next Action:" -ForegroundColor Cyan
Write-Host "   Press F5 in Visual Studio" -ForegroundColor White
Write-Host ""
Write-Host "Expected Result:" -ForegroundColor Cyan
Write-Host "  ? Migrations apply (FreshInitialMigration + AddFlattenedReportViewsFixed)" -ForegroundColor Green
Write-Host "  ? New views created with NVARCHAR fix" -ForegroundColor Green
Write-Host "  ? App starts successfully!" -ForegroundColor Green
Write-Host ""
Write-Host "?? Backup Location:" -ForegroundColor Cyan
Write-Host "   C:\Surveillance-Backups\BeforeMigrationFix" -ForegroundColor White
Write-Host ""
